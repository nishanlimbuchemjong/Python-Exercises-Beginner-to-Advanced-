# Use the sorted() function to sort the dictionary {1: 'b', 2: 'a', 3: 'c'} by its values.

dictionary = {1: 'b', 2: 'a', 3: 'c'}

# sorting the values of a given dictionary using sorted() built-in function
result = sorted(dictionary.values())

# printing out the result
print(f"Given dictionary is {dictionary}")
print(f"\nAfter using sorted() function for sorting above dictionary, we get\n{result}")